class RecipeInfo {
    constructor(pName, pId, pDescription, pTag, pIngredient, pStep, pNotes, pImage){
        this.name = pName;
        this.id = pId;
        this.description = pDescription;
        this.tag = pTag;
        this.ingredient = pIngredient;
        this.step = pStep;
        this.notes = pNotes;
        this.image = pImage;
    }
}